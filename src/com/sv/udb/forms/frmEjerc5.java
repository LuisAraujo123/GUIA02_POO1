    private void btnReduActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReduActionPerformed
        // TODO add your handling code here:
        if (this.objEjer.getIngresado().length() > 0){
            this.objEjer.setIngresado(this.objEjer.getIngresado().substring(0, this.objEjer.getIngresado().length() -1));
            txtIngre.setText(this.objEjer.getIngresado());
        }
        
    }//GEN-LAST:event_btnReduActionPerformed

    private void btnMaMnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMaMnActionPerformed
        // TODO add your handling code here:
        if(this.objEjer.getIngresado().contains("-")){
            this.objEjer.setIngresado(this.objEjer.getIngresado().substring(1));
            txtIngre.setText(this.objEjer.getIngresado());
        }
        else {
            this.objEjer.setIngresado("-"+this.objEjer.getIngresado());
            txtIngre.setText(this.objEjer.getIngresado());
        }
        
    }//GEN-LAST:event_btnMaMnActionPerformed

