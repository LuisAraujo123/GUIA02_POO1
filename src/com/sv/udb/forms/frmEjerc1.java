    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!("").equals(txtBase.getText().trim()) && !("").equals(txtPote.getText().trim())) {
            try {
                int base = Integer.parseInt(txtBase.getText().trim());
                int potencia = Integer.parseInt(txtPote.getText().trim());
                Ejercicio1 obje = new Ejercicio1();
                obje.setNumeroBase(base);
                obje.setNumeroPote(potencia);
                int resultado = obje.getResultado();
                JOptionPane.showMessageDialog(this, "Resultado de elevar " + base + " a la potencia " + potencia + ": " + resultado);
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Por favor solo ingrese numeros");
                e.printStackTrace();
            }
                   
        }
    }//GEN-LAST:event_jButton1ActionPerformed

